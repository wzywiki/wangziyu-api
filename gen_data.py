def make_id(seed: str) -> str:
    """生成类似 MongoDB ObjectId 格式的 24 位十六进制 ID"""
    h = hashlib.md5(seed.encode()).hexdigest()
    return h[:24]

def guess_language(name: str) -> str:
    if re.search(r'[\u3040-\u309f\u30a0-\u30ff]', name):
        return "国语"  # 日文假名但按原站归类
    if re.match(r'^[A-Za-z0-9\s\!\?\.\,\'\-\(\)]+$', name.strip()):
        return "英语"
    return "国语"

def guess_music_type(name: str) -> str:
    covers = ["练习", "翻唱"]
    osts = ["ED", "OP", "OST", "剧情", "预告", "群像", "盗墓笔记", "雪中悍刀行", "降灵记", "当我嫁给处女座"]
    for c in covers:
        if c in name:
            return "单曲&其他"
    for o in osts:
        if o in name:
            return "商业OST"
    return "单曲&其他"

def guess_solo(name: str) -> str:
    multi = ["串烧", "群像", "合唱"]
    for m in multi:
        if m in name:
            return "多人原唱"
    return "SOLO"

def fmt_time(t: str | None) -> str | None:
    if not t or t == "nan":
        return None
    try:
        d = datetime.strptime(t, "%Y-%m-%d")
        return d.strftime("%Y-%m-%dT00:00:00")
    except:
        return None

